            function validasi() {
                var nrp = document.getElementById("nrp").value;
                var nama = document.getElementById("nama").value;

                if (nrp.length == 0) {
                    Swal.fire({
                        title: "Kesalahan Input",
                        text: "NRP tidak boleh kosong",
                        icon: "error"
                    });
                    return false;
                }

                if (nama.length == 0) {
                    Swal.fire({
                        title: "Kesalahan Input",
                        text: "Nama tidak boleh kosong",
                        icon: "error"
                    });
                    return false;
                }

                if (nama.length < 2) {
                    Swal.fire({
                        title: "Kesalahan Input",
                        text: "Nama minimal 2 huruf",
                        icon: "error"
                    });
                    return false;
                }

                var regex = /\d/;
                if (regex.test(nama)) {
                    Swal.fire({
                        title: "Kesalahan Input",
                        text: "Nama harus semua huruf",
                        icon: "error"
                    });
                    return false;
                }

                return true;
            }
